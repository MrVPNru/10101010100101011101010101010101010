#!/bin/bash

clear

while true; do
echo "========================================"
echo "   CYBER-CODER v2.0 (Ультра-Кириллица)"
echo "========================================"
echo "1. Зашифровать текст в двоичный код"
echo "2. Расшифровать двоичный код в текст"
echo "========================================"
read -p "Выбери действие (1 или 2): " choice

if [ "$choice" == "1" ]; then
    read -p "Введи текст для шифрования: " user_text
    echo -n "Результат в бинарном виде: "
    # Переводим текст в двоичный вид напрямую через xxd и форматируем дефисами
    echo -n "$user_text" | xxd -b | cut -d: -f2 | cut -d" " -f2-8 | tr -d '\n' | tr -s ' ' '-' | sed 's/-$//'
    echo ""

elif [ "$choice" == "2" ]; then
    read -p "Введи двоичный код: " user_bin
    # Очищаем строку от дефисов и пробелов
    clean_bin=$(echo "$user_bin" | tr -d ' -')
    echo -n "Расшифрованный текст: "
    # Переводим обратно из бинарного в hex, а затем в текст
    echo "$clean_bin" | perl -lpe '$_=pack"B*",$_'
    echo ""
else
    echo "Ошибка: Используй только 1 или 2."
fi
done

